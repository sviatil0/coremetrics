static const Glyph *lookupGlyph(char c)
{
    char upper = static_cast<char>(std::toupper(static_cast<unsigned char>(c)));
    for (const Glyph &g : GLYPHS)
    {
        if (g.c == upper || g.c == c)
        {
            return &g;
        }
    }
    return nullptr;
}

static void drawGlyph(Screen &screen, const Glyph &g, ivec2 origin, vec3 color)
{
    for (int row = 0; row < GLYPH_HEIGHT; ++row)
    {
        unsigned char bits = g.rows[row];
        for (int col = 0; col < GLYPH_WIDTH; ++col)
        {
            if ((bits >> (GLYPH_WIDTH - 1 - col)) & 0x1)
            {
                ivec2 pxMin(origin.x + col * PIXEL_SIZE, origin.y + row * PIXEL_SIZE);
                ivec2 pxMax(pxMin.x + PIXEL_SIZE - 1, pxMin.y + PIXEL_SIZE - 1);
                screen.drawBox(pxMin, pxMax, color);
            }
        }
    }
}

Label::Label(std::string text, ivec2 pos, vec3 col) : m_text(std::move(text)), m_position(pos), m_color(col)
{
}

std::string Label::getText() const
{
    return m_text;
}

void Label::setText(std::string text)
{
    m_text = std::move(text);
}

void Label::draw(Screen &screen)
{
    for (std::size_t i = 0; i < m_text.length(); ++i)
    {
        char c = m_text[i];
        ivec2 charOrigin(m_position.x + static_cast<int>(i) * CHAR_ADVANCE, m_position.y);
        const Glyph *g = lookupGlyph(c);
        if (g != nullptr)
        {
            drawGlyph(screen, *g, charOrigin, m_color);
        }
        else
        {
            ivec2 boxMin = charOrigin;
            ivec2 boxMax(boxMin.x + GLYPH_WIDTH * PIXEL_SIZE - 1, boxMin.y + LINE_HEIGHT - 1);
            screen.drawBox(boxMin, boxMax, m_color);
        }
    }
}
